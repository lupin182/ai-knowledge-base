import puppeteer from 'puppeteer';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';
const __dirname = dirname(fileURLToPath(import.meta.url));
const errors = [];
const browser = await puppeteer.launch({ headless: 'new', args: ['--no-sandbox'] });
const page = await browser.newPage();
page.on('pageerror', (e) => errors.push('ERR ' + e.message));
page.on('requestfailed', (r) => errors.push('FAIL ' + r.url() + ' :: ' + (r.failure()?.errorText || '?')));
await page.setViewport({ width: 1440, height: 900 });
const url = 'http://localhost:4321/docs/tools/pdf-reader.html?pdf=kb/redacted-topic/papers/artgs.pdf&title=ArtGS%20%E2%80%94%E2%80%94%20per-instance%20%E5%85%B3%E8%8A%82';
console.log('→', url);
await page.goto(url, { waitUntil: 'networkidle0', timeout: 30000 });
await new Promise(r => setTimeout(r, 2000));
const title = await page.title();
const headerText = await page.evaluate(() => document.querySelector('.pdf-header, .pdf-title, h1, h2')?.textContent?.trim() || '');
const numPages = await page.evaluate(() => window.pdfDoc?.numPages || 'no-pdf');
console.log('  title:', title, '| header:', headerText, '| numPages:', numPages);
await page.screenshot({ path: resolve(__dirname, '..', 'screenshots', 'real', 'pdf-standalone.png'), fullPage: false });
console.log('errors:', errors.length ? errors : '(none)');
await browser.close();
